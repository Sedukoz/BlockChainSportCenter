    private void ModifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyButtonActionPerformed
        if(this.ModifyNameTxt.getText().isEmpty() || this.ModifySCodeTxt.getText().isEmpty() ||
                this.ModifyFeeTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields Required", "Empty Fields", JOptionPane.ERROR_MESSAGE);
            
        } else {
            if(SportTbl.getSelectedRow() >= 0){
                int row = SportTbl.getSelectedRow();
                int sportID = Integer.parseInt((String)model.getValueAt(row, 0));
                String sportname = (String)model.getValueAt(row, 1);
                String sportcode = (String)model.getValueAt(row, 2);
                String centercode = (String)model.getValueAt(row, 3);
                String centername = (String)model.getValueAt(row, 4);
                String fee = (String)model.getValueAt(row, 5);
                
                
                Sport sportEdit = new Sport(sportID, sportname, sportcode, centercode, centername, fee);
                Sport editedSport;
                editedSport = new Sport();
                editedSport.setSportID(sportID);
                editedSport.setSportName(this.ModifyNameTxt.getText());
                editedSport.setSportCode(this.ModifySCodeTxt.getText());
                editedSport.setCenterCode(centercode);
                editedSport.setCenterName(centername);
                editedSport.setFee(this.ModifyFeeTxt.getText());
                
                sportEdit.editSport(editedSport);
                JOptionPane.showMessageDialog(this, "Record Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
                ModifyNameTxt.setText("");
                ModifySCodeTxt.setText("");
                ModifyFeeTxt.setText("");
                
     
            } else {
                JOptionPane.showMessageDialog(this, "Please select a record!", "Error", JOptionPane.ERROR_MESSAGE);
                
            }
        }
        model.setRowCount(0);


    }//GEN-LAST:event_ModifyButtonActionPerformed

